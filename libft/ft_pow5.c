unsigned long	ft_pow5(int pow)
{
	int res;

	res = 1;
	if (pow == 0)
		return (1);
	if (pow < 0)
		return (-1);
	while (pow)
	{
		res *= 5;
		pow--;
	}
	return (res);
}
